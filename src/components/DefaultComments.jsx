function seedRandom(seed) {
  var x = Math.sin(seed++) * 10000;
  return x - Math.floor(x);
}
// Function to get 3 random comments from a category
export function getRandomComments(category, seed) {
  console.log("Randoms Begin");
  console.log(category, seed);
  const categoryComments = comments[category];
  if (!categoryComments) {
    return [];
  }
  console.log("Randoms Begin");

  // Create a copy of the array
  const copy = [...categoryComments];

  // Use the Fisher-Yates (aka Knuth) Shuffle algorithm
  let count = copy.length;
  while (count) {
    let i = Math.floor(seedRandom(seed) * count--);
    [copy[count], copy[i]] = [copy[i], copy[count]];
    seed++;
  }
  console.log("Randoms End");
  console.log(copy.slice(0, 3));
  console.log(seed);

  return copy.slice(0, 3);
}
